/**
 * ============================================================
 * 《万界行者》核心NPC角色卡
 * Narrative Design by NarrativeDesigner
 * ============================================================
 *
 * 角色卡设计标准：
 * 1. 每个角色必须有"核心创伤"——什么塑造了他们的世界观
 * 2. 每个角色必须有"声音支柱"——词汇、节奏、避讳话题、语言习惯
 * 3. 每个角色必须是玩家的"镜子"——反映某种玩家可能的选择或可能性
 * 4. 每个角色必须有"绝不会说的话"——用于检测对话是否OOC
 */

// ============================================================
// 角色卡接口定义
// ============================================================

export interface CharacterVoicePillars {
  /** 词汇偏好：正式/随意、技术/口语、地域特色 */
  vocabulary: string;
  /** 句子节奏：短促/绵长、急促/缓慢 */
  sentenceRhythm: string;
  /** 避讳话题：这个角色永远不会直接谈论什么 */
  topicsAvoided: string[];
  /** 语言习惯：口头禅、停顿方式、特定短语 */
  verbalTics: string[];
  /** 潜台词默认：直接表达还是绕弯子 */
  subtextDefault: string;
}

export interface CharacterCard {
  id: string;
  name: string;
  /** 角色在故事中的定位 */
  role: string;
  /** 核心创伤：什么塑造了这个世界观 */
  coreWound: string;
  /** 有意识想要的是什么 */
  desire: string;
  /** 真正需要的是什么（通常与desire矛盾） */
  need: string;
  /** 声音支柱 */
  voice: CharacterVoicePillars;
  /** 绝不会说的话（用于检测OOC） */
  wouldNeverSay: { line: string; reason: string }[];
  /** 参考台词（声音范例） */
  referenceLines: { line: string; demonstrates: string }[];
  /** 与玩家的关系发展弧线 */
  relationshipArc: string;
  /** 关键剧情节点 */
  keyStoryNodes: string[];
  /** 作为玩家的"镜子"——反映什么 */
  mirrorToPlayer: string;
}

