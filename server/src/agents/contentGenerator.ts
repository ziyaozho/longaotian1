export const generateTask = (context: AgentContext): Task | null => {
  if (!checkProbability(0.5)) return null;

  // 根据玩家等级和场景筛选合适的任务
  const availableTasks = TASK_TEMPLATES.filter(t => {
    if (t.type === 'main' && context.player.stats.level < 5) return false;
    return true;
  });

  const template = randomChoice(availableTasks);
  const difficulty = randomInt(1, 3);

  // 根据目标类型调整目标值
  let targetValue = template.targetValue;
  if (template.targetType === 'level') {
    targetValue = context.player.stats.level + randomInt(2, 5);
  } else if (template.targetType === 'wealth') {
    targetValue = context.player.stats.wealth + randomInt(200, 500);
  } else {
    targetValue = Math.max(1, template.targetValue + randomInt(0, 2));
  }

  let targetRounds = template.type === 'main' ? randomInt(3, 6) : template.type === 'side' ? randomInt(2, 4) : randomInt(1, 3);

  // wealth和level类型任务一旦达标即完成，不需要多回合积累
  if (template.targetType === 'wealth' || template.targetType === 'level') {
    targetRounds = 1;
  }
  // combat和social类型通常需要多次完成
  if (template.targetType === 'combat' || template.targetType === 'social') {
    targetRounds = Math.max(1, template.targetValue + randomInt(0, 1));
  }

  return {
    id: `task_${Date.now()}_${randomInt(1000, 9999)}`,
    name: template.name,
    description: `${template.description}（目标：${getTaskTargetDesc(template.targetType, targetValue)}）`,
    type: template.type,
    targetType: template.targetType,
    targetValue,
    difficulty,
    targetRounds,
    progress: 0,
    reward: {
      exp: difficulty * 60 * context.player.progress.sceneLevel,
      wealth: difficulty * 40 * context.player.progress.sceneLevel,
      systemExp: difficulty * 10,
    },
    completed: false,
  };
};

function getTaskTargetDesc(targetType: string, value: number): string {
  switch (targetType) {
    case 'survive': return `存活${value}回合`;
    case 'combat': return `战斗胜利${value}次`;
    case 'wealth': return `财富达到${value}`;
    case 'level': return `等级达到${value}`;
    case 'explore': return `探索${value}次`;
    case 'social': return `完成${value}次社交`;
    default: return `完成${value}目标`;
  }
}
