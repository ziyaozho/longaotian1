interface DynamicChoice {
  id: string;
  text: string;
  consequence?: string;
}

// ---- 最近使用的选项类别追踪（避免连续重复） ----

const recentCategoryUsed: Record<string, string[]> = {};

function pickWithAntiRepeat<T extends { id: string }>(
  pool: T[],
  categoryPrefix: string,
  count: number,
  sceneType: string,
): T[] {
  if (!recentCategoryUsed[sceneType]) {
    recentCategoryUsed[sceneType] = [];
  }
  const recent = recentCategoryUsed[sceneType];

  // 优先选择最近未用过的类别中的选项
  const fresh = pool.filter((c) => !recent.includes(c.id));
  const candidates = fresh.length >= count ? fresh : pool;

  const shuffled = [...candidates].sort(() => Math.random() - 0.5);
  const selected = shuffled.slice(0, Math.min(count, shuffled.length));

  // 记录本次使用的类别
  recentCategoryUsed[sceneType] = [
    ...recent,
    categoryPrefix,
  ].slice(-6);

  return selected;
}

