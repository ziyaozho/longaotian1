export const generateSceneContent = (context: AgentContext): GeneratedContent => {
  const templates = SCENE_TEMPLATES[context.sceneType] || SCENE_TEMPLATES['modern_city'];
  const sceneLevel = context.player.progress.sceneLevel;

  // 根据境界层级选择模板池
  let pool: string[] = [...templates.base];
  if (sceneLevel >= 2) pool.push(...templates.early);
  if (sceneLevel >= 4) pool.push(...templates.mid);
  if (sceneLevel >= 7) pool.push(...templates.late);
  if (sceneLevel >= 10) pool.push(...templates.peak);

  const baseText = randomChoice(pool);

  // 使用动态选项生成
  const choices = generateDynamicChoices(context, baseText);

  return {
    text: baseText,
    choices,
    effects: {},
  };
};

export const generateEvent = (context: AgentContext): GeneratedContent | null => {
  if (!checkProbability(0.4)) return null;

  const events = EVENT_TEMPLATES[context.sceneType] || EVENT_TEMPLATES['modern_city'];
  if (!events || events.length === 0) return null;

  // 过滤掉不适合当前状态的事件
  const availableEvents = events.filter(e => !e.condition || e.condition(context));
  const eventPool = availableEvents.length > 0 ? availableEvents : events;

  const event = randomChoice(eventPool);
  const choices: Choice[] = event.choices.map((text, i) => ({
    id: `e${i}`,
    text,
  }));

  return {
    text: event.desc,
    choices,
    effects: {},
  };
};

export const generateSystemMessage = (context: AgentContext): SystemResponse => {
  const sys = getSystemById(context.player.system.id);
  const messages = sys ? (SYSTEM_MESSAGES[sys.id] || SYSTEM_MESSAGES['sign_in']) : SYSTEM_MESSAGES['sign_in'];
  const message = randomChoice(messages)
    .replace('X', String(randomInt(1, 30)));

  const types: SystemResponse['type'][] = ['info', 'reward', 'upgrade', 'warning'];
  const type = randomChoice(types);

  let rewards = undefined;
  if (type === 'reward') {
    rewards = {
      exp: randomInt(10, 50) * context.player.system.level,
      wealth: randomInt(0, 100) * context.player.system.level,
    };
  } else if (type === 'upgrade') {
    rewards = {
      systemExp: randomInt(5, 20),
    };
  }

  return {
    message,
    type,
    rewards,
  };
};

