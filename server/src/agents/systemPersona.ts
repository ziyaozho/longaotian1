// ============================================================
// 成长节奏模板 —— 参考系统文"三起三落"结构
// ============================================================

export type StoryBeat = 'humble_start' | 'small_win' | 'setback' | 'big_breakthrough' | 'crush';

export interface GrowthRhythm {
  /** 根据回合数 + 当前阶段 判断应该是什么剧情节奏 */
  beat: StoryBeat;
  /** 对应的叙事基调 */
  tone: string;
  /** 数值加成倍率（影响AI返回的属性变化大小） */
  statMultiplier: number;
}

/**
 * 系统文经典成长节奏：
 * 开局平凡(1-5回合) → 获得小机缘(6-10) → 遇到挫折(11-15) → 大突破(16-20) → 碾压(21-25) → 循环
 */
export function determineStoryBeat(
  round: number,
  level: number,
  lastBeatWasSetback: boolean
): GrowthRhythm {
  const cycle = round % 25;

  if (cycle <= 5) {
    return { beat: 'humble_start', tone: '平凡日常，小人物视角，暗流涌动', statMultiplier: 0.5 };
  }
  if (cycle <= 10) {
    return { beat: 'small_win', tone: '获得小机缘，初次尝到系统甜头，信心初建', statMultiplier: 1.0 };
  }
  if (cycle <= 15) {
    return { beat: 'setback', tone: '遇到更强的对手或困境，需要智慧+系统才能度过', statMultiplier: -0.5 };
  }
  if (cycle <= 20) {
    return { beat: 'big_breakthrough', tone: '逆境中爆发，实力大幅跃升，震惊周围', statMultiplier: 3.0 };
  }
  return { beat: 'crush', tone: '碾压之前不可一世的对手，装逼打脸，扬眉吐气', statMultiplier: 2.0 };
}

/**
 * 根据剧情节拍选择最适合的系统人格
 */
export function selectPersonaTemplate(personality: string): SystemPersonaTemplate {
  if (personality.includes('毒舌') || personality.includes('吐槽') || personality.includes('反套路')) return ANTI_CLICHE_PERSONA;
  if (personality.includes('热血') || personality.includes('霸气') || personality.includes('天选')) return CHOSEN_ONE_PERSONA;
  if (personality.includes('吞噬') || personality.includes('冷静')) return DEVOURER_PERSONA;
  if (personality.includes('反派') || personality.includes('冷酷')) return VILLAIN_PERSONA;
  if (personality.includes('导师') || personality.includes('温柔') || personality.includes('教学')) return MENTOR_PERSONA;
  if (personality.includes('召唤') || personality.includes('统帅')) return SUMMONER_PERSONA;
  return MENTOR_PERSONA; // 默认导师系统
}
