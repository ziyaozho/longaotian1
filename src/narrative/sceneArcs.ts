/**
 * ============================================================
 * 《万界行者》8个场景主线剧情弧
 * Narrative Design by NarrativeDesigner
 * ============================================================
 *
 * 每个世界的主线剧情弧包含：
 * - 开场事件（Entry Event）：玩家如何进入这个世界，初始身份和冲突
 * - 中期转折（Midpoint Twist）：世界观被颠覆的时刻
 * - 本源凝聚（Essence Climax）：获得世界本源的关键剧情
 * - 告别时刻（Departure）：离开世界前的情感高潮
 *
 * 设计原则：
 * 1. 每个世界的剧情必须与该世界的"主题"和"腐蚀方式"呼应
 * 2. 每个世界至少有一个让玩家情感投入的关键NPC
 * 3. 剧情中的选择会影响系统与玩家的关系
 * 4. 每个世界的告别都应该留下某种"未完成的牵挂"
 */

// ============================================================
// 剧情弧接口定义
// ============================================================

export interface StoryChoice {
  id: string;
  text: string;
  /** 这个选择反映的玩家价值观 */
  valueReflected: string;
  /** 短期后果（本世界内） */
  shortTermConsequence: string;
  /** 长期后果（跨世界影响） */
  longTermConsequence?: string;
}

export interface StoryNode {
  id: string;
  title: string;
  description: string;
  /** 戏剧功能：揭示、建立关系、制造压力、传递后果 */
  dramaticFunction: 'reveal' | 'establish_relationship' | 'create_pressure' | 'deliver_consequence' | 'climax' | 'resolution';
  /** 涉及的关键NPC */
  involvedNPCs: string[];
  /** 玩家的选择（如果有） */
  choices?: StoryChoice[];
  /** 必须前置完成的节点 */
  prerequisites?: string[];
}

export interface SceneArc {
  worldId: string;
  worldName: string;
  /** 这个世界的核心主题 */
  theme: string;
  /** 玩家在这个世界的身份 */
  walkerRole: string;
  /** 虚无之噬的腐蚀方式 */
  corruptionMethod: string;
  /** 四个关键剧情节点 */
  entryEvent: StoryNode;
  midpointTwist: StoryNode;
  essenceClimax: StoryNode;
  departure: StoryNode;
  /** 情感弧线 */
  emotionalArc: string;
}

